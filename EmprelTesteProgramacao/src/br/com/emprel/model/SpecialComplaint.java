package br.com.emprel.model;

public class SpecialComplaint extends Complaint {
	private int Age;
	private String SchooLevel;
	private String Ocuppation;
	private String EventAddress;
	private String EventComplement;
	private String EventProvince;
	private String EventCity;
	private String EventState;
	private String EventZipCode;
	private String EventPhone;

	public int getAge() {
		return Age;
	}

	public void setAge(int age) {
		Age = age;
	}

	public String getSchooLevel() {
		return SchooLevel;
	}

	public void setSchooLevel(String schooLevel) {
		SchooLevel = schooLevel;
	}

	public String getOcuppation() {
		return Ocuppation;
	}

	public void setOcuppation(String ocuppation) {
		Ocuppation = ocuppation;
	}

	public String getEventAddress() {
		return EventAddress;
	}

	public void setEventAddress(String eventAddress) {
		EventAddress = eventAddress;
	}

	public String getEventComplement() {
		return EventComplement;
	}

	public void setEventComplement(String eventComplement) {
		EventComplement = eventComplement;
	}

	public String getEventProvince() {
		return EventProvince;
	}

	public void setEventProvince(String eventProvince) {
		EventProvince = eventProvince;
	}

	public String getEventCity() {
		return EventCity;
	}

	public void setEventCity(String eventCity) {
		EventCity = eventCity;
	}

	public String getEventState() {
		return EventState;
	}

	public void setEventState(String eventState) {
		EventState = eventState;
	}

	public String getEventZipCode() {
		return EventZipCode;
	}

	public void setEventZipCode(String eventZipCode) {
		EventZipCode = eventZipCode;
	}

	public String getEventPhone() {
		return EventPhone;
	}

	public void setEventPhone(String eventPhone) {
		EventPhone = eventPhone;
	}
}
